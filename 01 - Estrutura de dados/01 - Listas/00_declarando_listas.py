# declaração de listas, estrutura de dados mutável, ordenada, indexada, permite elementos duplicados
# podemos criar listas usando colchetes [] ou a função list()
# colocando elementos entre colchetes, separados por vírgula, criamos uma lista
# listas são objetos mutáveis, ou seja, podemos alterar seus elementos após a criação da lista

frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = []
print(frutas)

letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
