# A lista é uma sequencia de elementos, onde cada elemento tem um índice, começando do zero.
# Podemos acessar os elementos da lista usando seus índices, a partir do zero, ou seja, o primeiro elemento tem índice 0, o segundo elemento tem índice 1, e assim por diante.

frutas = ["maçã", "laranja", "uva", "pera"]

print(frutas[0])  # maçã
print(frutas[2])  # uva
