# Contando o número de elementos em tuplas. O número de elementos em uma tupla pode ser obtido utilizando a função len().
# A função len() é uma função embutida em Python que retorna o número de itens em um objeto.
# - Quando aplicada a uma tupla, ela retorna o número de elementos presentes na tupla.

linguagens = (
    "python",
    "js",
    "c",
    "java",
    "csharp",
)

print(len(linguagens))  # 5
