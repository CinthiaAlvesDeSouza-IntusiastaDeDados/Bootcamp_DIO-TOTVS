# Acessando elementos de uma tupla. 
# Assim como as listas, as tuplas também são indexadas, ou seja, cada elemento tem um índice que começa em 0. 
# Para acessar um elemento da tupla, basta utilizar o índice entre colchetes [].

frutas = ("maçã", "laranja", "uva", "pera",)

print(frutas[0])  # maçã
print(frutas[2])  # uva
