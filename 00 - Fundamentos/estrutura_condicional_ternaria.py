# A estrutura condicional ternária em Python é uma forma concisa de escrever uma expressão condicional. Ela permite que você avalie uma condição e retorne um valor com base nessa condição, tudo em uma única linha de código. A sintaxe é a seguinte:

saldo = 2000
saque = 1500

status = "Sucesso" if saldo >= saque else "Falha"

print(f"{status} ao realizar o saque!")
