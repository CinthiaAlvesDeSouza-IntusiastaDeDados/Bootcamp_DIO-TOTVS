saldo = 200
saque = 200

print(saldo == saque) # Verifica se os valores são iguais
print(saldo != saque) # Verifica se os valores são diferentes
print(saldo > saque) # Verifica se o valor de saldo é maior que o valor de saque
print(saldo >= saque) # Verifica se o valor de saldo é maior ou igual ao valor de saque
print(saldo < saque) # Verifica se o valor de saldo é menor que o valor de saque
print(saldo <= saque) # Verifica se o valor de saldo é menor ou igual ao valor de saque
