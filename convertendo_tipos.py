print(int(1.97348728)) # converte o número 1.97348728 para um número inteiro, resultando em 1
print(int("10")) # converte a string "10" para um número inteiro, resultando em 10
print(float("10.10")) # converte a string "10.10" para um número de ponto flutuante, resultando em 10.10
print(float(100)) # converte o número inteiro 100 para um número de ponto flutuante, resultando em 100.0

valor = 10
valor_str = str(valor) # converte o número inteiro 10 para uma string, resultando em "10"
print(type(valor)) # imprime o tipo da variável valor, que é <class 'int'>
print(type(valor_str)) # imprime o tipo da variável valor_str, que é <class 'str'>  
print(type(str(10.10)))  

print(100 / 2) # resultado é 50.0, pois a divisão de dois números inteiros resulta em um número de ponto flutuante
print(100 // 2) # resultado é 50, pois a divisão inteira de dois números inteiros resulta em um número inteiro